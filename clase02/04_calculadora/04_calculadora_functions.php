<?php
    function sumar($valor01,$valor02){
        return  $valor01 +  $valor02;
    }
    function restar($valor01,$valor02){
        return  $valor01 -  $valor02;
    }
    function multiplicar($valor01,$valor02){
        return  $valor01 *  $valor02;
    }
    function dividir($valor01,$valor02){
        return  $valor01 /  $valor02;
    }
?>